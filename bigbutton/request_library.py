def request_hidden(url):
    """
    Function hides the request as a browser request so that the site does not block
    from: https://medium.com/@raiyanquaium/how-to-web-scrape-using-beautiful-soup-in-python-without-running-into-http-error-403-554875e5abed
    :param url:
    :return:
    """
    user_agent = random.choice(user_agent_list)

    r = requests.get(url,
                    headers={'User-Agent':user_agent})

    if r.status_code != 200:
        print('##Warning: request failed with code %s' %r.status_code)
        print('User agent was %s'%user_agent)

    return r

